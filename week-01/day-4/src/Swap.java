public class Swap {
    public static void main(String[] args) {
        // Swap the values of the variables
        int a = 100;
        int b = 777;
        System.out.println("The A is: "+a);
        System.out.println("The B is: "+b);
        int c=a;
        a=b;
        b=c;
        System.out.println("The A is: "+a);
        System.out.println("The B is: "+b);



    }
}
